// Fungsi helper untuk mendapatkan gambar berdasarkan kategori dan konteks
export const getBusinessImage = (category: keyof typeof businessImages, subCategory?: string) => {
  if (subCategory && businessImages[category] && typeof businessImages[category] === 'object') {
    return (businessImages[category] as any)[subCategory];
  }
  return businessImages[category];
};

// Fungsi untuk mendapatkan portrait commissioner berdasarkan nama
export const getCommissionerBusinessImage = (name: string) => {
  const commissioner = businessImages.commissioners.find(c => c.name === name);
  return commissioner?.image || businessImages.commissioners[0].image;
};

// Fungsi untuk mendapatkan portrait pejabat berdasarkan nama
export const getPejabatBusinessImage = (name: string) => {
  const pejabat = businessImages.pejabat.find(p => p.name === name);
  return pejabat?.image || businessImages.pejabat[0].image;
};
