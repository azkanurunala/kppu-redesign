// Fungsi helper untuk mendapatkan gambar berdasarkan kategori dan konteks
export const getImage = (category: keyof typeof imageConfig, subCategory?: string) => {
  if (subCategory && imageConfig[category] && typeof imageConfig[category] === 'object') {
    return (imageConfig[category] as any)[subCategory];
  }
  return imageConfig[category];
};

// Fungsi untuk mendapatkan portrait commissioner berdasarkan nama
export const getCommissionerImage = (name: string) => {
  const commissioner = imageConfig.commissioners.find(c => c.name === name);
  return commissioner?.image || imageConfig.commissioners[0].image;
};

// Fungsi untuk mendapatkan portrait pejabat berdasarkan nama
export const getPejabatImage = (name: string) => {
  const pejabat = imageConfig.pejabat.find(p => p.name === name);
  return pejabat?.image || imageConfig.pejabat[0].image;
};
