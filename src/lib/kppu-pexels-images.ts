// Fungsi untuk mendapatkan header image dengan mapping khusus
export const getHeaderImageForPage = (pathname: string) => {
  const { headerImages } = kppuPexelsImages;
  
  // Cek apakah ada mapping khusus
  if (pageHeaderMapping[pathname] !== undefined) {
    return headerImages[pageHeaderMapping[pathname]];
  }
  
  // Fallback ke sistem hash
  return getHeaderImageByPath(pathname);
};
