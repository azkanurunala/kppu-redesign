// Fungsi helper untuk mendapatkan gambar berdasarkan kategori dan konteks
export const getKppuPexelsImage = (category: keyof typeof kppuPexelsImages, subCategory?: string) => {
  if (subCategory && kppuPexelsImages[category] && typeof kppuPexelsImages[category] === 'object') {
    return (kppuPexelsImages[category] as any)[subCategory];
  }
  return kppuPexelsImages[category];
};

// Fungsi untuk mendapatkan portrait commissioner berdasarkan nama
export const getCommissionerKppuPexelsImage = (name: string) => {
  const commissioner = kppuPexelsImages.commissioners.find(c => c.name === name);
  return commissioner?.image || kppuPexelsImages.commissioners[0].image;
};

// Fungsi untuk mendapatkan portrait pejabat berdasarkan nama
export const getPejabatKppuPexelsImage = (name: string) => {
  const pejabat = kppuPexelsImages.pejabat.find(p => p.name === name);
  return pejabat?.image || kppuPexelsImages.pejabat[0].image;
};

// Fungsi untuk mendapatkan header image berdasarkan path halaman
// Menggunakan hash sederhana untuk konsistensi tapi tetap "random"
export const getHeaderImageByPath = (pathname: string) => {
  const { headerImages } = kppuPexelsImages;
  
  // Buat hash sederhana dari pathname
  let hash = 0;
  for (let i = 0; i < pathname.length; i++) {
    const char = pathname.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32bit integer
  }
  
  // Ambil index berdasarkan hash
  const index = Math.abs(hash) % headerImages.length;
  return headerImages[index];
};

